                        <h2><br>Craniotomy </h2>
                        <p>In surgical treatment, an opening is made in the skull. This is called a craniotomy. The 
                        surgeon tries to remove the entire tumor. A portion of the scalp is shaved in craniotomy, 
                        and an incision is made through the skin. Then a piece of bone is removed to expose the 
                        area of the brain over the tumor. The dura mater is opened, and the tumor is located and 
                        removed. The bone is replaced, and the scalp is stitched. </p>
                        
                        <p><br>If the entire tumor cannot be removed completely without damaging the brain tissues. 
                            Then the doctor removes as much of the tumor they can remove, and then other 
                            treatments are provided to cure the condition. 
                             
