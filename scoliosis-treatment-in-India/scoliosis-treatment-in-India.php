                                       <h2><br>Type of scoliosis  </h2>  
                                       <p><b>Idiopathic scoliosis-</b> The causative agent cannot be determined. Idiopathic scoliosis 
                                        can develop from childhood, and it can be divided into early-onset or late-onset. 
                                        Idiopathic scoliosis can be classified as infantile, juvenile, or adolescent.  </p>
                                         
                                        <p><br><b>Adolescent scoliosis-</b> This type of scoliosis occurs between 10 to 18 years of age 
                                        common among females, usually affecting the upper spine and the rib cage. If the 
                                        adolescent scoliosis is major, it can affect lung and severe scoliosis and increases the 
                                        chances of the curve progression.</p> 
                                         
                                        <p><br><b>Adult Idiopathic Scoliosis-</b> In this condition, the patient suffers from scoliosis and 
                                        curve spine problems from childhood. </p>
                                         
                                        <p><br><b>Degenerative Scoliosis-</b> This condition develops in adulthood and occurs as a result of 
                                        disc and facet joint degeneration. The degeneration of the disc causes them to lose 
                                        height. If one side of the disc degenerates rapidly, then another side, this causes the disc 
                                        to tilt and causes bending of the spine. </p>
                                         
                                        <p><br><b>Adult scoliosis-</b> Large curves cause more back pain than smaller curves. If the curve 
                                        formed is more than 80 degrees. It also causes shortness of breath, and it impairs the 
                                        function of the lungs. Adult scoliosis can also cause narrowing around the nerves or 
                                        spinal stenosis in the lumbar spine and can cause root compression. Its symptom 
                                        includes numbness, weakness, and leg pain. </p>
                                         
                                        <p><br><b>Neuromuscular scoliosis-</b> It is caused by a disorder of the nerve or muscles in the 
                                        spine or trunk. It can cause an abnormal sideways curve where the spine grows. 
                                        Cerebral palsy and muscular dystrophies can cause neuromuscular scoliosis. </p>
                                         
                                        <p><br><b>Congenital scoliosis-</b> Abnormally shaped vertebrae causes it at birth. It can be noticed 
                                        before 10 years. </p>
                                         
                                        <p><br><b>Syndromic scoliosis-</b>It is caused by genetic disorders that affect the ligaments, 
                                        muscles, and bones. This can disturb the stability of the spine.</p> 

