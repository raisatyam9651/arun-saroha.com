                        <h2><br>Artificial Disc Replacement Surgery in Delhi (India) </h2>
                        <p></p>In this treatment, the disc (soft material between two vertebrae) is removed, is replaced 
                        with the bone graft to cure the pain. Then with time, the bone creates bony solid bridges 
                        across the vertebra. This surgery helps to make the painful segment pain free. This 
                        surgery helps to stabilize the painful spine and helps correct back pain issues. </p>
                        
                        <p><br>The surgery is performed under general anesthesia, and the procedure takes about 1-2 
                        hours. In the first step of treatment, the diseased disc is removed before the installation 
                        of the artificial disc. The surgeon makes an incision in the abdomen area to approach 
                        the lower back. This helps to move the organs and blood vessels to the side. This also 
                        allows the surgeon to access the spine without moving the nerves. Then the disc space 
                        is opened and exposed. Then the problematic disk is removed, and an artificial disk 
                        implant is placed in the disk space. For different parts of the vertebrae, the disc 
                        prosthesis will differ. </p>
                        
                        <p><br>An alternative to disc replacement called disc nucleus replacement is performed often 
                        when the outer part of the disc is healthy. In this procedure, the center of the original 
                        disc is removed and is replaced by a plastic that absorbs water and swells, and helps fill 
                        the cavity of the nucleus. </p>
                        
                        <p><br>After surgery, the recovery period can last from 6 to eight weeks.</p> 
 
                        <p><br>One can perform light activities after surgery, but it is very important that one avoid 
                        activities that cause one to hyperextend their back. </p>
                        
