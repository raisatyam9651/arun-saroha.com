                     <h2><br>Treatment for Brain Aneurysm in Delhi (India)  </h2>
                     <p>Many a time unruptured aneurysm does not show any symptoms and the following test can help detect the condition. The tests include. <br></p>
                     <p><br><b>CT scan or Computed tomography -</b>A CT scan is a specialized X-ray exam that helps to determine bleeding in the brain, and it produces 2D images of the brain. </p>
                     <p><br><b>Computed Tomography Angiogram (CTA) scan-</b> It is also a most preside method of evaluating brain blood vessels and is a standard form of CT. This test combines using CT scans and special computers and a contrast dye that is injected into the brain to show the images of blood vessels. </p>
                     <p><br><b>MRI or Magnetic Resonance Imaging- </b>It is similar to a computed tomography angiogram.  This treatment uses magnetic field radio waves to create detailed images of the brain structures and vessels. </p> 
                     <p><br><b>Angiogram of the brain-</b> This is a type of X-Ray test in which a catheter is inserted into the brain blood vessels into the grain that further reaches the affected area. Then a dyeis injected into the artery. Then images reveal the details about the ruptured aneurysm. </p> 
                 
