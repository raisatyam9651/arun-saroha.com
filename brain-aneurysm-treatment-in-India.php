                    <h2><br>Surgical Brain Aneurysm Treatment in Delhi (India) </h2>
                    <p><b>Clipping- </b>A small section of the skull is removed. This helps to access the affected area with asylum and to 
                    locate damaged blood vessels. Then a small tiny metal clip is placed at the affected region on the neck of the aneurysm. 
                    This helps to prevent or stop the blood flow and close off the aneurysm. It also prevents further growth or rupture.</p>
                    <p><br><b>Endovascular coiling or embolization- </b>It is less invasive than surgical clipping. Many a time, flow diversion
                    devices are also used to cure aneurysm. In this method, a hollow  tube made of plastic is inserted into the artery through 
                    the groin and using threads, it reaches the affected target site of the body. Then a wire is pushed into the aneurysm  using a 
                    catheter. The wire is coiled, and it stops the blood flow and causes blood to clot.</p>
