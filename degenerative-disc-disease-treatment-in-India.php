                                        present between the vertebrae of the spinal cord break down or deteriorate and often 
                                        cause pain. </p>
                                         
                                        <p>The rubbery discs between the vertebrae allow the flexibility and bending of the back 
                                        area and shock absorbers. This condition involves the intervertebral or spinal discs. 
                                        Degenerative disc disease is not really a disease, but it is normal wear and tear 
                                        condition caused due to aging in the spine.  The discs are located in between the 
                                        vertebrae of the spine and act as cushions and shock absorbers.</p>   
                                         
                                        <p>A degenerative disc disease problem occurs within one or more of the intervertebral 
                                        discs. The disc is present between each of the vertebrae in the spinal cord. The discs act 
                                        as shock absorbers, and without the cushion effect of the discs, the vertebrae in the 
                                        spine cannot absorb the stresses. The stress is transferred to the discs, and it causes 
                                        injuries to the disc. </p>
                                        <p>The spinal discs provide padding between the vertebrae of the spine. The outer part of 
                                        the disc is called annulus fibrosus, and its outer portion contains nerves. Any damage to 
                                        this layer can cause disc tears that are quite painful. The inner core of the disc is called 
                                        nucleus pulposus and is soft and gelatinous. </p>
