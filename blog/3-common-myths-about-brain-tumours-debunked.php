                <p><strong>The Fact:</strong> While primary brain tumours do originate in the brain tissue, many brain tumours are actually <strong>secondary (metastatic)</strong> tumours. These are cancers that start in another part of the body—such as the lungs, breast, colon, or kidneys—and then spread (metastasize) to the brain. In adults, metastatic tumours are actually more common than primary ones.</p>
