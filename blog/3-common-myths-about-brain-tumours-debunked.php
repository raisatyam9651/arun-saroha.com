                <p><strong>The Fact:</strong> Not all brain tumours are cancerous or "malignant." Brain tumours are broadly classified into two main types: **benign (non-cancerous)** and **malignant (cancerous)**. Benign tumours grow slowly and are less likely to spread to other tissues. While even a benign tumour can cause serious issues depending on its size and location, modern medical advancements allow for many of these tumours to be treated effectively and often cured completely.</p>
            </div>

